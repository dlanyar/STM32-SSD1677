void set_time ()  // monday = 1
{
    RTC_TimeTypeDef sTime = {0};
    RTC_DateTypeDef sDate = {0};

    // Définir l'heure (ex: 14:30:00)
    sTime.Hours = 23;
    sTime.Minutes = 59;
    sTime.Seconds = 45;
    HAL_RTC_SetTime(&hrtc, &sTime, RTC_FORMAT_BIN);

    // Définir la date (ex: 25 Décembre 2023, Lundi)
    sDate.WeekDay = RTC_WEEKDAY_MONDAY;
    sDate.Month = RTC_MONTH_JANUARY;
    sDate.Date = 25;
    sDate.Year = 25; // 2023
    HAL_RTC_SetDate(&hrtc, &sDate, RTC_FORMAT_BIN);

}

void get_time_date(void)
{
    RTC_DateTypeDef gDate;
    RTC_TimeTypeDef gTime;
    char time_buf[6];
    char date_buf[20];

    /* Lecture RTC */
    HAL_StatusTypeDef status = HAL_RTC_GetTime(&hrtc, &gTime, RTC_FORMAT_BIN);
    if(status != HAL_OK) return;

    status = HAL_RTC_GetDate(&hrtc, &gDate, RTC_FORMAT_BIN);
    if(status != HAL_OK) return;

    /* Formatage pour le LCD */
    // Heure (Ligne 1)
    snprintf(time_buf, sizeof(time_buf), "%02d:%02d",
            gTime.Hours, gTime.Minutes);

    // Date (Ligne 2)
    snprintf(date_buf, sizeof(date_buf), "Date:%02d-%02d-%04d",
            gDate.Date, gDate.Month, 2000 + gDate.Year);

    Paint_Clear(&paint,UNCOLORED);
    Paint_DrawStringAt(&paint,80,65,time_buf,&Font72,COLORED);
    DisplayFrame_Part(Paint_GetImage(&paint), 24, 24, 264, 464, false);
    SSD1677_TurnOnDisplay();
    HAL_Delay(10000);

}
